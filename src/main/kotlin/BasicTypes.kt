fun main() {
    val number: Int = 5
    val unsignedNumber: UInt = 10u     // Declaración de una variable entera sin signo (UInt)
    val pi: Double = 3.14       // Variable de tipo decimal
    val isKotlin: Boolean = true
    val greeting: String = "Hi"
    val letter: Char = 'A'    // Declaración de una variable de tipo caracter (Char)

    println("Number: $number")
    println("Unsigned Number: $unsignedNumber")
    println("Pi: $pi")
    println("Is Kotlin? $isKotlin")
    println("Greeting: $greeting")
    println("Letter: $letter")
}

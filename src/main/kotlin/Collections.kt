// 3. Collections
fun main() {
    // Listas
    val lista = listOf(1, 2, 3, 4)  // Crea una lista inmutable con elementos del 1 al 4
    println("Lista: $lista")

    // Sets
    val conjunto = setOf("a", "b", "c")  // Crea un conjunto inmutable, no permite elementos duplicados
    println("Set: $conjunto")

    // Mapas
    val mapa = mapOf("clave1" to "valor1", "clave2" to "valor2") // Crea un mapa inmutable donde cada clave est√° asociada a su correspondiente valor

    println("Mapa: $mapa")
}
